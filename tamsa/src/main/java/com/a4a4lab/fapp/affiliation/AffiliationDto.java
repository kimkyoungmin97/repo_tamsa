package com.a4a4lab.fapp.affiliation;

public class AffiliationDto {
	
	private String seq;
	private Integer representativeAffiliation;
	private String company;
	private String department;
	private String position;
	private Integer addContact_seq;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	
	public Integer getRepresentativeAffiliation() {
		return representativeAffiliation;
	}
	public void setRepresentativeAffiliation(Integer representativeAffiliation) {
		this.representativeAffiliation = representativeAffiliation;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public Integer getAddContact_seq() {
		return addContact_seq;
	}
	public void setAddContact_seq(Integer addContact_seq) {
		this.addContact_seq = addContact_seq;
	}
	
	

}
