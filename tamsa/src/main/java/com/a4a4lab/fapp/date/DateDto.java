package com.a4a4lab.fapp.date;

public class DateDto {
	
	private String seq;
	private Integer representativeDate;
	private String dateType;
	private String date;
	private Integer solarCalendarNy;
	private Integer addContact_seq;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public Integer getRepresentativeDate() {
		return representativeDate;
	}
	public void setRepresentativeDate(Integer representativeDate) {
		this.representativeDate = representativeDate;
	}
	public String getDateType() {
		return dateType;
	}
	public void setDateType(String dateType) {
		this.dateType = dateType;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public Integer getSolarCalendarNy() {
		return solarCalendarNy;
	}
	public void setSolarCalendarNy(Integer solarCalendarNy) {
		this.solarCalendarNy = solarCalendarNy;
	}
	public Integer getAddContact_seq() {
		return addContact_seq;
	}
	public void setAddContact_seq(Integer addContact_seq) {
		this.addContact_seq = addContact_seq;
	}
	
	

}
