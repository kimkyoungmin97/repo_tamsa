package com.a4a4lab.fapp.homepage;

public class HomepageDto {
	
	private String seq;
	private String homepage;
	private Integer addContact_seq;
	private Integer representativeHomepage;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getHomepage() {
		return homepage;
	}
	public void setHomepage(String homepage) {
		this.homepage = homepage;
	}
	public Integer getAddContact_seq() {
		return addContact_seq;
	}
	public void setAddContact_seq(Integer addContact_seq) {
		this.addContact_seq = addContact_seq;
	}
	public Integer getRepresentativeHomepage() {
		return representativeHomepage;
	}
	public void setRepresentativeHomepage(Integer representativeHomepage) {
		this.representativeHomepage = representativeHomepage;
	}
	
	

}
