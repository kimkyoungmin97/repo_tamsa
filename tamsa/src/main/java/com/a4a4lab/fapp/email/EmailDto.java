package com.a4a4lab.fapp.email;


public class EmailDto {
	
	private String seq;
	private Integer representativeEmail;
	private String email;
	private Integer addContact_seq;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public Integer getRepresentativeEmail() {
		return representativeEmail;
	}
	public void setRepresentativeEmail(Integer representativeEmail) {
		this.representativeEmail = representativeEmail;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public Integer getAddContact_seq() {
		return addContact_seq;
	}
	public void setAddContact_seq(Integer addContact_seq) {
		this.addContact_seq = addContact_seq;
	}
	
	

}
