package com.a4a4lab.fapp.sns;

public class SnsDto {
	
	private String seq;
	private Integer representativeSns;
	private String snsType;
	private String sns;
	private Integer addContact_seq;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public Integer getRepresentativeSns() {
		return representativeSns;
	}
	public void setRepresentativeSns(Integer representativeSns) {
		this.representativeSns = representativeSns;
	}
	public String getSnsType() {
		return snsType;
	}
	public void setSnsType(String snsType) {
		this.snsType = snsType;
	}
	public String getSns() {
		return sns;
	}
	public void setSns(String sns) {
		this.sns = sns;
	}
	public Integer getAddContact_seq() {
		return addContact_seq;
	}
	public void setAddContact_seq(Integer addContact_seq) {
		this.addContact_seq = addContact_seq;
	}
	

}
